export function getHoomans(blog) {
  // If not music blog return
  if (!blog.isMusic) return;

  const metadata = JSON.parse(blog.metadata);
  if (!metadata.significantHoomans.length > 0) return;

  const blogHoomans = JSON.parse(blog.metadata).significantHoomans.map((name) =>
    name.toLowerCase()
  );
  return significantHoomans.filter((hooman) => {
    const nameMatch = blogHoomans.includes(hooman.name.toLowerCase());

    // Check if any synonym matches
    const synonymMatch = hooman.synonyms.some((synonym) =>
      blogHoomans.includes(synonym.toLowerCase())
    );

    return nameMatch || synonymMatch;
  });
}

export const formatHoomans = (hoomans) => {
  if (!hoomans.length > 0) return [];
  if (typeof hoomans !== "string") return hoomans;
  const splitArr = hoomans.split(",").map((keyword) => keyword.trim());
  return splitArr;
};
